export const getAllConsultationServices = () =>
  CONSULTATION_CATEGORIES.flatMap((cat) =>
    cat.cards.map((card) => ({
      ...card,
      categoryId: cat.id,
      category: cat.name,
    }))
  );

export const getConsultationServiceById = (serviceId) =>
  getAllConsultationServices().find((s) => s.id === serviceId) || null;
